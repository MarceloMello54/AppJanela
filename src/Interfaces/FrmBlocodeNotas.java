    private void save() {

        // exibir caixa de dialogo
        int selecao = jFileChooser.showSaveDialog(jMenuSalvar);

        if (selecao == 0) {

            try {
                BufferedReader ler = null;
                File arquivo = jFileChooser.getSelectedFile();
                PrintWriter escrever = new PrintWriter(arquivo);
                escrever.print(jTextArea.getText());
                escrever.close();
            } catch (FileNotFoundException ex) {
            }
        }

    }


