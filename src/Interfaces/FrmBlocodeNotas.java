    private void jMenuAbrirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jMenuAbrirActionPerformed
 // exibi caixa de dialogo para abrir arquivo 
       
int selecao = jFileChooser.showOpenDialog(jMenuAbrir);

        if (selecao == 0) {

            try {
                BufferedReader ler = null;
                File arquivo = jFileChooser.getSelectedFile();
                ler = new BufferedReader(new FileReader(arquivo));

                String linha = ler.readLine();
                while (linha != null) {
                    jTextArea.append(linha);
                    jTextArea.append(System.getProperty("line.separador"));
                    linha = ler.readLine();
                }
                ler.close();

            } catch (FileNotFoundException ex) {

            } catch (IOException ex) {
            }

        }
    }//GEN-LAST:event_jMenuAbrirActionPerformed

